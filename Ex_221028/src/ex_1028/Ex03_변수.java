package ex_1028;

public class Ex03_변수 {

	public static void main(String[] args) {
		// Ctrl + D = 줄삭제
		// Ctrl + Alt + ↓ or ↑ = 줄복사
		// Alt + ↓ or ↑ = 줄이동
		// Ctrl + f = 단어찾기
		// Ctrl + Shift + f = 띄어쓰기 정렬

		int numbar = 200;

		System.out.println(numbar + 2); // ; = 코드 하나별로 컴파일 하는데 코드를 구분해주는 구분자
		System.out.println(numbar - 2);
		System.out.println(numbar * 2);
		System.out.println(numbar / 2);

	}

}
