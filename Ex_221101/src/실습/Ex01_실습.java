package 실습;

public class Ex01_실습 {

	public static void main(String[] args) {

		for (int i = 21; i <= 57; i++) {
			System.out.print(i + " ");
		}
		System.out.println();
		for (int k = 96; k >= 53; k--) {
			System.out.print(k + " ");
		}
		System.out.println();
		for (int j = 21; j <= 57; j++) {
			if (j % 2 == 1)
				System.out.print(j + " ");
		}

	}

}
